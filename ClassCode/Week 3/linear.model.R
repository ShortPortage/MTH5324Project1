
###########################################################
# Simulation of a linear regression model
###########################################################

###########################################################
# True Underlying Model
###########################################################
x = seq(-5,10,by=0.5)
mu.y = -3 + 2*x
plot(x,mu.y)

###########################################################
# Observation Model
###########################################################
obs.y = mu.y + rnorm(length(mu.y))
plot(x,obs.y)

###########################################################
# Estimated Model
###########################################################
reg.mdl = lm(obs.y~x)
summary(reg.mdl)

